def soma_argumentos(n1,n2,n3):
    resultado=n1+n2+n3
    return resultado
def multiplicar_argumentos(n1,n2,n3):
    resultado=n1*n2*n3
    return resultado

numero1= float(input('Digite um numero aqui'))
numero2= float(input('Digite um segundo numero aqui'))
numero3= float(input('Digite um terceiro numero aqui'))
print(soma_argumentos(numero1,numero2,numero3))
print(multiplicar_argumentos(numero1,numero2,numero3))


