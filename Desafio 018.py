from math import radians,sin,cos,tan
an=float(input('Insira aqui o valor do Ângulo:'))
sen=sin(radians(an))
cos=cos(radians(an))
tan=tan(radians(an))
print('O Ângulo {}, possui um SENO de {:.2f}'.format( an,sen))
print('o ângulo {}, possui um COSSENO de {:.2f}'.format(an,cos))
print('O ângulo {}, possui uma TANGENTE de {:.2f}'.format(an,tan))










